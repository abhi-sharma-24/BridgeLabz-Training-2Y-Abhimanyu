import static java.lang.Math.*;

public class staticclass {
    public static void main(String[] args) {
        double a = 16.0;
        double b = 2.0;

        System.out.println("sqrt(a): " + sqrt(a));        // sqrt()
        System.out.println("pow(a,b): " + pow(a,b));      // pow()
        System.out.println("max(a,b): " + max(a,b));      // max()
        System.out.println("min(a,b): " + min(a,b));      // min()
        System.out.println("abs(-a): " + abs(-a));        // abs()
    }
}
